<p>
    To use google API, we need to create some secret key. There are so many google APIs and we need to create a project
    and enable API that we want to use and create oauth client id as our secret key.
</p>
<ul>
    <li>
        Go to <a href="https://console.cloud.google.com/getting-started" target="_blank">google cloud console</a>
    </li>
    <li>Create project</li>
    <li>Enable Gmail API</li>
    <li>Create OAuth consent screen</li>
    <li>Create OAuth client ID</li>
    <li>Copy the Client ID and Client Secret below</li>
    <li>Ready to use Gmail API</li>
</ul>
